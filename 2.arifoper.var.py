#арифметические операции
x = 1
y = 2

res = x + y # сумма 
print(res)

res = x - y # вычитание
print(res)

# обычное деление
res = x/y #всегда показывает запятую
print(res)

# целочисленное деление
res = x//y
print(res)

#деление по остатку - полезно при счетчиках
res = x % y
print(res)

# возведение в степень
res = x ** y
print(res)

